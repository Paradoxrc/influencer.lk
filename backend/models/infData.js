import mongoose from "mongoose";

const InfDataSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
}, { timestamps: true });

const InfData = mongoose.model("InfData", InfDataSchema);

export default InfData;
